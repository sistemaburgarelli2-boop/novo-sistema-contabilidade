export function getSecao(code: string): string {
  const n = parseInt(code.slice(0, 4), 10);
  if (n <= 399)  return "A";
  if (n <= 999)  return "B";
  if (n <= 3399) return "C";
  if (n <= 3530) return "D";
  if (n <= 3900) return "E";
  if (n <= 4399) return "F";
  if (n <= 4799) return "G";
  if (n <= 5399) return "H";
  if (n <= 5699) return "I";
  if (n <= 6399) return "J";
  if (n <= 6699) return "K";
  if (n <= 6899) return "L";
  if (n <= 7599) return "M";
  if (n <= 8299) return "N";
  if (n <= 8499) return "O";
  if (n <= 8599) return "P";
  if (n <= 8899) return "Q";
  if (n <= 9399) return "R";
  if (n <= 9699) return "S";
  if (n <= 9799) return "T";
  return "U";
}

